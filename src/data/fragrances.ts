export function getFragranceById(id: string): Fragrance | undefined {
  return fragrances.find((f) => f.id === id);
}

export function searchFragrances(query: string): Fragrance[] {
  const q = query.toLowerCase();
  return fragrances.filter(
    (f) =>
      f.name.toLowerCase().includes(q) ||
      f.brand.toLowerCase().includes(q) ||
      f.notes.some((n) => n.name.toLowerCase().includes(q)) ||
      f.scentFamily.some((sf) => sf.includes(q))
  );
}
